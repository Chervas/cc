import { Component, OnInit, OnDestroy } from '@angular/core';
import { CommonModule } from '@angular/common';
import { MatButtonModule } from '@angular/material/button';
import { MatCardModule } from '@angular/material/card';
import { MatIconModule } from '@angular/material/icon';
import { Subject } from 'rxjs';
import { takeUntil } from 'rxjs/operators';

// ‚úÖ USAR TIPOS VERIFICADOS
import { RoleService, Usuario, UsuarioClinicaResponse } from 'app/core/services/role.service';

@Component({
    selector: 'app-role-test',
    templateUrl: './role-test-component.html',
    imports: [
        CommonModule,
        MatButtonModule,
        MatCardModule,
        MatIconModule
    ],
    standalone: true
})
export class RoleTestComponent implements OnInit, OnDestroy {

    private destroy$ = new Subject<void>();
    
    // ‚úÖ PROPIEDADES USANDO TIPOS VERIFICADOS
    currentUser: Usuario | null = null;
    clinicas: UsuarioClinicaResponse[] = [];
    selectedRole: string | null = null;
    selectedClinica: UsuarioClinicaResponse | null = null;
    testResults: any = {};

    constructor(private roleService: RoleService) {}

    ngOnInit(): void {
        console.log('üß™ RoleTestComponent: Inicializando con datos reales del backend...');
        this.loadRealData();
    }

    ngOnDestroy(): void {
        this.destroy$.next();
        this.destroy$.complete();
    }

    // ‚úÖ CARGAR DATOS REALES DEL BACKEND - VERIFICADO
    private loadRealData(): void {
        this.roleService.currentUser$.pipe(
            takeUntil(this.destroy$)
        ).subscribe(user => {
            this.currentUser = user;
            this.log(`üë§ Usuario actual: ${user?.nombre} ${user?.apellidos} (ID: ${user?.id_usuario})`);
        });

        this.roleService.clinicasConRol$.pipe(
            takeUntil(this.destroy$)
        ).subscribe(clinicas => {
            this.clinicas = clinicas;
            this.log(`üè• Cl√≠nicas disponibles: ${clinicas.length}`);
            clinicas.forEach(clinica => {
                // ‚úÖ USAR PROPIEDADES REALES - VERIFICADO
                this.log(`  - ${clinica.name} (${clinica.userRole})`);  // ‚Üê PROPIEDADES REALES
            });
        });

        this.roleService.selectedRole$.pipe(
            takeUntil(this.destroy$)
        ).subscribe(role => {
            this.selectedRole = role;
            this.log(`üé≠ Rol seleccionado: ${role}`);
            console.log('üîç [DEBUG] Rol actual del usuario:', this.getCurrentRole());
            console.log('üîç [DEBUG] ¬øTiene rol admin?', this.roleService.hasRole('administrador'));
            console.log('üîç [DEBUG] ¬øTiene rol propietario?', this.roleService.hasRole('propietario'));
            console.log('üîç [DEBUG] ¬øTiene rol administrador?', this.roleService.hasRole('administrador'));
            console.log('üîç [DEBUG] ¬øTiene rol personaldeclinica?', this.roleService.hasRole('personaldeclinica'));
        });

        this.roleService.selectedClinica$.pipe(
            takeUntil(this.destroy$)
        ).subscribe(clinica => {
            this.selectedClinica = clinica;
            // ‚úÖ USAR PROPIEDADES REALES - VERIFICADO
            this.log(`üè• Cl√≠nica seleccionada: ${clinica?.name}`);  // ‚Üê PROPIEDAD REAL
        });
    }

    getCurrentRole(): string {
        const role = this.roleService.getCurrentRole();
        return role || 'Sin rol';
    }

    // ‚úÖ TEST DE ROLES USANDO PROPIEDADES REALES - VERIFICADO
    testRoles(): void {
        this.log('üß™ Iniciando test de roles con valores reales...');
        
        const rolesReales: string[] = ['administrador', 'propietario', 'doctor', 'personal', 'paciente'];
        
        this.testResults.roles = {};

        rolesReales.forEach(role => {
            const hasRole = this.roleService.hasRole(role);
            this.testResults.roles[role] = hasRole;
            this.log(`üß™ Rol ${role}: ${hasRole ? '‚úÖ' : '‚ùå'}`);
        });

        const isAdmin = this.roleService.isAdmin();
        this.testResults.isAdmin = isAdmin;
        this.log(`üß™ Es Admin: ${isAdmin ? '‚úÖ' : '‚ùå'}`);
    }

    testPermissions(): void {
        this.log('üß™ Iniciando test de permisos basados en roles reales...');
        
        const permissions = this.roleService.getCurrentPermissions();
        this.testResults.permissions = permissions;
        
        this.log(`üß™ Permisos actuales (${permissions.length}):`);
        permissions.forEach(permission => {
            this.log(`  - ${permission}`);
        });
    }

    testAdvancedPermissions(): void {
        this.log('üß™ Iniciando test de permisos avanzados...');
        
        this.testResults.advancedPermissions = {};
        
        const permissionGroups = [
            ['clinic.manage', 'users.manage'],
            ['patients.view', 'appointments.view'],
            ['settings.modify']
        ];

        permissionGroups.forEach((group, index) => {
            const currentPermissions = this.roleService.getCurrentPermissions();
            const hasAllPermissions = group.every(permission => 
                currentPermissions.includes(permission)
            );
            
            this.testResults.advancedPermissions[`group_${index}`] = {
                permissions: group,
                hasAll: hasAllPermissions
            };
            
            this.log(`üß™ Grupo ${index}: ${group.join(', ')} ‚Üí ${hasAllPermissions ? '‚úÖ' : '‚ùå'}`);
        });
    }

    // ‚úÖ TEST DE CL√çNICAS POR ROL USANDO PROPIEDADES REALES - VERIFICADO
    testClinicasByRole(): void {
        this.log('üß™ Iniciando test de cl√≠nicas por rol...');
        
        this.testResults.clinicasByRole = {};
        
        const rolesReales: string[] = ['administrador', 'propietario', 'doctor', 'personal', 'paciente'];
        
        rolesReales.forEach(role => {
            const clinicas = this.roleService.getClinicasByRole(role);
            this.testResults.clinicasByRole[role] = clinicas;
            
            this.log(`üß™ Cl√≠nicas como ${role}: ${clinicas.length}`);
            clinicas.forEach(clinica => {
                // ‚úÖ USAR PROPIEDADES REALES - VERIFICADO
                this.log(`  - ${clinica.name} (${clinica.userSubRole || 'Sin subrol'})`);  // ‚Üê PROPIEDADES REALES
            });
        });
    }

    changeRole(role: string): void {
        this.log(`üß™ Cambiando rol a: ${role}`);
        this.roleService.selectRole(role);
    }

    // ‚úÖ CAMBIAR CL√çNICA USANDO PROPIEDADES REALES - VERIFICADO
    changeClinica(clinica: UsuarioClinicaResponse): void {
        this.log(`üß™ Cambiando cl√≠nica a: ${clinica.name}`);  // ‚Üê PROPIEDAD REAL
        this.roleService.selectClinica(clinica);
    }

    // ‚úÖ OBTENER ROLES DISPONIBLES USANDO PROPIEDADES REALES - VERIFICADO
    getAvailableRoles(): string[] {
        return this.clinicas.map(clinica => clinica.userRole)  // ‚Üê PROPIEDAD REAL
            .filter((role, index, array) => array.indexOf(role) === index);
    }

    getUserInfo(): string {
        if (!this.currentUser) return 'No hay usuario cargado';
        
        return `${this.currentUser.nombre} ${this.currentUser.apellidos} (${this.currentUser.email_usuario})`;
    }

    // ‚úÖ OBTENER INFORMACI√ìN DE LA CL√çNICA USANDO PROPIEDADES REALES - VERIFICADO
    getClinicaInfo(): string {
        if (!this.selectedClinica) return 'No hay cl√≠nica seleccionada';
        
        return `${this.selectedClinica.name} - ${this.selectedClinica.userRole}${  // ‚Üê PROPIEDADES REALES
            this.selectedClinica.userSubRole ? ` (${this.selectedClinica.userSubRole})` : ''  // ‚Üê PROPIEDAD REAL
        }`;
    }

    clearResults(): void {
        this.testResults = {};
        this.log('üß™ Resultados limpiados');
    }

    getResultsJson(): string {
        return JSON.stringify(this.testResults, null, 2);
    }

    hasResults(): boolean {
        return Object.keys(this.testResults).length > 0;
    }

    reloadData(): void {
        this.log('üß™ Recargando datos del backend...');
        this.clearResults();
    }

    private log(message: string): void {
        console.log(message);
    }

    // ‚úÖ OBTENER ESTAD√çSTICAS USANDO PROPIEDADES REALES - VERIFICADO
    getStats(): any {
        return {
            totalClinicas: this.clinicas.length,
            rolesUnicos: this.getAvailableRoles().length,
            rolActual: this.selectedRole,
            clinicaActual: this.selectedClinica?.name || 'Ninguna',  // ‚Üê PROPIEDAD REAL
            esAdmin: this.roleService.isAdmin(),
            permisos: this.roleService.getCurrentPermissions().length
        };
    }

    testRolesReales(): void {
        this.log('üß™ Iniciando test de roles reales del backend...');
        
        this.testResults.rolesReales = {};
        
        const rolesReales: string[] = ['administrador', 'propietario', 'doctor', 'personal', 'paciente'];

        rolesReales.forEach(role => {
            const hasRole = this.roleService.hasRole(role);
            const level = this.roleService.getRoleLevel(role);
            const label = this.roleService.getRoleLabel(role);
            const color = this.roleService.getRoleColor(role);
            const icon = this.roleService.getRoleIcon(role);
            
            this.testResults.rolesReales[role] = {
                hasRole,
                level,
                label,
                color,
                icon
            };
            
            this.log(`üß™ Rol ${role}: ${hasRole ? '‚úÖ' : '‚ùå'} (Nivel: ${level}, Label: ${label})`);
        });
    }

    runAllTests(): void {
        this.log('üß™ Ejecutando todos los tests...');
        this.clearResults();
        
        this.testRoles();
        this.testPermissions();
        this.testAdvancedPermissions();
        this.testClinicasByRole();
        this.testRolesReales();
        
        this.log('üß™ Todos los tests completados');
    }

    testRoleLevels(): void {
        this.log('üß™ Iniciando test de niveles de roles...');
        
        this.testResults.roleLevels = {};
        
        const rolesReales: string[] = ['administrador', 'propietario', 'doctor', 'personal', 'paciente'];
        
        rolesReales.forEach(role => {
            const level = this.roleService.getRoleLevel(role);
            this.testResults.roleLevels[role] = level;
            this.log(`üß™ Nivel de ${role}: ${level}`);
        });
    }

    testRoleDisplay(): void {
        this.log('üß™ Iniciando test de display de roles...');
        
        this.testResults.roleDisplay = {};
        
        const rolesReales: string[] = ['administrador', 'propietario', 'doctor', 'personal', 'paciente'];
        
        rolesReales.forEach(role => {
            this.testResults.roleDisplay[role] = {
                label: this.roleService.getRoleLabel(role),
                color: this.roleService.getRoleColor(role),
                icon: this.roleService.getRoleIcon(role)
            };
            
            this.log(`üß™ Display ${role}: ${this.roleService.getRoleLabel(role)} (${this.roleService.getRoleColor(role)})`);
        });
    }
}

